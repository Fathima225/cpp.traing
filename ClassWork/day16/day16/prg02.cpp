#include<iostream>
using namespace std;

int main()
{
	int max = 0, min = 0;
	int a[] = { 10,20,30,40,50,5 };
	for (int i = 0;i <= a[i];i++) {
		if (max < a[i]) {
			max = a[i];

		}
		else(min > a[i]); {
			min = a[i];
			

		}
	}
	cout << "max" << max << endl;

	cout << "min" << min << endl;
	return 0;
}